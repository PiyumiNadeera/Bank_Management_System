    public void accountSelecting() {
        PreparedStatement ps;
        ResultSet rs;

        int regNumber = Integer.parseInt(jLabel40.getText());

        String query1 = "SELECT*FROM account_details WHERE registrationNumber = ?";
        try {
            ps = conn.prepareStatement(query1);
            ps.setInt(1, regNumber);
            rs = ps.executeQuery();

            while (rs.next()) {
                int accNumber = rs.getInt(2);
                jComboBox2.addItem(Integer.toString(accNumber));
            }
        } catch (SQLException ex) {
            Logger.getLogger(SavingsAccounts.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

