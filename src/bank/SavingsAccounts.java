    public Boolean mandatoryFields() {

        nameWithInitials = jTextField2.getText();
        fullName = jTextField6.getText();
        nic = jTextField1.getText();

        if (jRadioButton2.isSelected()) {
            gender = "Male";
        } else if (jRadioButton1.isSelected()) {
            gender = "Female";
        }

        teleNumber = jTextField4.getText();
        email = jTextField5.getText();

        if (jRadioButton3.isSelected()) {
            accountType = "18+";
        } else if (jRadioButton7.isSelected()) {
            accountType = "Senior Citizen";
        }

        if (jRadioButton6.isSelected()) {
            accountPurpose = "Business";
        } else if (jRadioButton4.isSelected()) {
            accountPurpose = "Savings";
        } else if (jRadioButton5.isSelected()) {
            accountPurpose = "Other";
        }

        if (jRadioButton9.isSelected()) {
            debitCard = "Yes";
        } else if (jRadioButton8.isSelected()) {
            debitCard = "No";
        }

        nomineeTitle = jComboBox1.getSelectedItem().toString();
        nomineeName = jTextField8.getText();
        nomineeAddress = jTextField3.getText();
        nomineeNIC = jTextField7.getText();

        if (jCheckBox1.isSelected()) {
            if (!nameWithInitials.isEmpty() && !fullName.isEmpty() && !nic.isEmpty() && (jRadioButton1.isSelected() ^ jRadioButton2.isSelected()) && (jRadioButton9.isSelected() ^ jRadioButton8.isSelected()) && !teleNumber.isEmpty() && !email.isEmpty() && !nomineeName.isEmpty() && !nomineeNIC.isEmpty()) {
                return true;
            } else {
                JOptionPane.showMessageDialog(null, "One or more fiellds are empty");
                return false;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Verification required");
            return false;
        }

    }

