    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        userName = jTextField1.getText();
        password = jPasswordField1.getText().toString();

        if (userName.isEmpty() || password.isEmpty()) {
            if (userName.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please enter the user name");
            } else if (password.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please enter the password");
            }
        } else {

            PreparedStatement ps;
            ResultSet rs;

            String Query = "SELECT*FROM registration WHERE BINARY userName = ? AND BINARY password =sha1(?)";
            try {
                ps = conn.prepareStatement(Query);
                ps.setString(1, userName);
                ps.setString(2, password);
                rs = ps.executeQuery();

                if (rs.next()) {
                    DashBoard form = new DashBoard();
                    form.setVisible(true);
                    form.pack();
                    form.setLocationRelativeTo(null);
                    this.dispose();

                    int registrationNumber = rs.getInt(1);
                    String title = rs.getString(2);
                    String nameWithInitials = rs.getString(3);

                    DashBoard.jLabel9.setText(nameWithInitials);
                    DashBoard.jLabel10.setText(Integer.toString(registrationNumber));
                    form.setVisible(true);

                } else {
                    JOptionPane.showMessageDialog(null, "Logging failed.Please check your user name and password");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

