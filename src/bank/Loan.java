    public Boolean mandatoryFields() {

        if (!jCheckBox1.isSelected()) {
            JOptionPane.showMessageDialog(null, "Please verify the given details");
            return false;
        } else {
            if (jTextField8.getText().isEmpty() || jTextField10.getText().isEmpty() || jTextField11.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField7.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "One or more fields are empty");
                return false;
            } else {
                return true;
            }
        }

    }

    public ArrayList<LoanDetails> getLoanList() {

        String loanNum = jComboBox3.getSelectedItem().toString();
        PreparedStatement ps;
        ResultSet rs;

        ArrayList<LoanDetails> LoanList = new ArrayList<LoanDetails>();

        try {
            String query1 = "SELECT*FROM loans WHERE loanNumber = ?";
            ps = conn.prepareStatement(query1);
            ps.setString(1, loanNum);
            rs = ps.executeQuery();

            LoanDetails Details;

            if (rs.next()) {
                Details = new LoanDetails(rs.getString(3), rs.getString(2), rs.getFloat(6), rs.getString(5), rs.getString(7), rs.getString(11), rs.getString(12), rs.getString(13));
                LoanList.add(Details);

            } else {
                JOptionPane.showMessageDialog(null, "No loans found");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Loan.class.getName()).log(Level.SEVERE, null, ex);
        }
        return LoanList;
    }

    public void showLoanTable() {
        ArrayList<LoanDetails> list = getLoanList();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        Object[] row = new Object[8];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).LDate();
            row[1] = list.get(i).LNum();
            row[2] = list.get(i).LAmount();
            row[3] = list.get(i).LPeriod();
            row[4] = list.get(i).LRate();
            row[5] = list.get(i).LDocCompletion();
            row[6] = list.get(i).LApproval();
            row[7] = list.get(i).LStatus();

            model.addRow(row);
        }
    }

