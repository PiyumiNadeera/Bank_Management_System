    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (mandatoryFields()) {
            String date = jLabel36.getText();
            int regNumber = Integer.parseInt(jLabel38.getText());
            if (jRadioButton3.isSelected()) {
                lType = "Personal Loan";
                // lRate = "9.50%";

            } else if (jRadioButton7.isSelected()) {
                lType = "Educaitonal Loan";
                //lRate = "4.50%";
            } else if (jRadioButton10.isSelected()) {
                lType = "Housing Loan";
                // lRate = "11.25%";
            } else {
                lType = "Business Loan";
                //  lRate = "10.75%";
            }
            // jTextField12.setText(lRate);
            lAmount = Float.parseFloat(jTextField8.getText());
            lPeriod = jTextField10.getText();
            lRate = jTextField12.getText();
            lPurpose = jTextField11.getText();
            guarantorTitle = jComboBox1.getSelectedItem().toString();
            guarantorName = jTextField3.getText().toUpperCase();
            guarantorNIC = jTextField7.getText().toUpperCase();

            PreparedStatement ps;
            ResultSet rs;
            try {
                String query1 = "INSERT INTO loans (registrationNumber,date,loanType,amount,period,interestRate,purpose,GuarantorName,GuarantorNIC,documentCompletion,approvalStatus,loanStatus) VALUES (?,?,?,?,?,?,?,?,?,?,?,?)";
                ps = conn.prepareStatement(query1);
                ps.setInt(1, regNumber);
                ps.setString(2, date);
                ps.setString(3, lType);
                ps.setFloat(4, lAmount);
                ps.setString(5, lPeriod);
                ps.setString(6, lRate);
                ps.setString(7, lPurpose);
                ps.setString(8, guarantorTitle + guarantorName);
                ps.setString(9, guarantorNIC);
                ps.setString(10, "NOT COMPLETED");
                ps.setString(11, "NOT APPROVED");
                ps.setString(12, "ON");

                if (ps.executeUpdate() == 1) {
                    JOptionPane.showMessageDialog(null, "Application successfully sent");
                    jTextField8.setText("");
                    jTextField10.setText("");
                    jTextField12.setText("");
                    jTextField11.setText("");
                    jTextField3.setText("");
                    jTextField7.setText("");
                    jRadioButton3.setSelected(false);
                    jRadioButton7.setSelected(false);
                    jRadioButton10.setSelected(false);
                    jRadioButton11.setSelected(false);

                } else {
                    JOptionPane.showMessageDialog(null, "Something went wrong");
                }

            } catch (SQLException ex) {
                Logger.getLogger(Loan.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

